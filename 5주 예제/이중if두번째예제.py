
num1=(input("첫 번째 숫자 입력 : "))             # 두 개의 서로 다른 숫자를 문자열로 입력
num2=(input("두 번째 숫자 입력 : "))

if num1 > num2 :                               # 문자열을 비교. 문자열의 코드 값으로 비교
    # 변수의 값이 문자열이므로 + 연산자를 사용하여 출력 가능
    print("두 수 중에 큰 수는 "+num1+"이고 작은 수는 "+num2+"입니다")

    
else :
    print("두 수 중에 큰 수는 "+num2+"이고 작은 수는 "+num1+"입니다") 

          
    
